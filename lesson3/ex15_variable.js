// '='는 변수를 저장한다는 의미
// 같음을 표현하고 싶다면 '==' 또는 '===' 사용 ex. a == b
a = 1;
console.log(a); // 1

var b;
console.log(b); // undefined

var c = 1;
console.log(c); // 1
c = 2;
console.log(c); // 2

let d = 1;
console.log(d); // 1
d = 2;
console.log(d); // 2

const e = 1;
console.log(e); // 1
// e = 2; // error!